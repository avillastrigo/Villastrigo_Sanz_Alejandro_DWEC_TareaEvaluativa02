  let laberintoActual = [];
let jugadorX = 0, jugadorY = 0, metaX = 0, metaY = 0, tiempoRestante = 180, cuentaRegresivaInterval;
let canMove = false; // Controla si el jugador puede moverse

const laberintoDiv = document.getElementById("laberinto");
const cuentaRegresivaDiv = document.getElementById("cuentaRegresiva");
const startBtn = document.getElementById("start-btn");
const audio = document.getElementById("bg-music");
const audioToggle = document.getElementById("audio-toggle");

// Función para generar el laberinto (como la tenías antes)
function generarLaberinto() {
  laberintoDiv.innerHTML = '';
  laberintoActual.forEach((fila, x) => {
      fila.forEach((celda, y) => {
          const div = document.createElement("div");
          div.className = "celda " + (celda === 1 ? "pared" : "camino");
          if (x === metaX && y === metaY) div.classList.add("meta");
          if (x === jugadorX && y === jugadorY) div.classList.add("jugador");
          laberintoDiv.appendChild(div);
      });
  });
}

// Función para mover al jugador
function moverJugador(dx, dy) {
  const nuevaX = jugadorX + dx;
  const nuevaY = jugadorY + dy;
  if (laberintoActual[nuevaX] && laberintoActual[nuevaX][nuevaY] === 0 && canMove) { // Solo permite mover si canMove es true
      jugadorX = nuevaX;
      jugadorY = nuevaY;
      generarLaberinto();
      if (jugadorX === metaX && jugadorY === metaY) {
          clearInterval(cuentaRegresivaInterval);
          alert("¡Has ganado!");
          window.location.href = `ganaste.html`;
      }
  }
}

// Control de teclas para mover al jugador
document.addEventListener("keydown", (e) => {
  if (!canMove) return; // Evita el movimiento si no se ha hecho clic en "Start"
  switch (e.key) {
      case "ArrowUp": moverJugador(-1, 0); break;
      case "ArrowDown": moverJugador(1, 0); break;
      case "ArrowLeft": moverJugador(0, -1); break;
      case "ArrowRight": moverJugador(0, 1); break;
  }
});

// Función para iniciar el nivel
function iniciarNivel(nivel) {
  laberintoDiv.className = `nivel-${nivel}`;
  const config = configuraciones[nivel];
  laberintoActual = laberintos[nivel];
  jugadorX = config.inicio.x;
  jugadorY = config.inicio.y;
  metaX = config.meta.x;
  metaY = config.meta.y;
  tiempoRestante = config.tiempo;

  generarLaberinto();
  // La cuenta regresiva no se inicia aún
}

// Función para iniciar la cuenta regresiva
function iniciarCuentaRegresiva() {
  cuentaRegresivaInterval = setInterval(() => {
      if (tiempoRestante > 0) {
          tiempoRestante--;
          cuentaRegresivaDiv.textContent = `${Math.floor(tiempoRestante / 60)}:${tiempoRestante % 60}`;
      } else {
          clearInterval(cuentaRegresivaInterval);
          alert("¡Has perdido!");
          window.location.href = `perdiste.html`;
      }
  }, 1000);
}

// Función para controlar la música
audioToggle.addEventListener("click", () => {
  if (audio.muted) {
    audio.muted = false;
    audio.play();
    audioToggle.classList.remove("fa-volume-up");
    audioToggle.classList.add("fa-volume-mute");
  } else {
    audio.muted = true;
    audioToggle.classList.remove("fa-volume-mute");
    audioToggle.classList.add("fa-volume-up");
  }
});

// Función para comenzar el juego y la cuenta regresiva
startBtn.addEventListener("click", () => {
  // Reproducir la música y quitar el mute
  audio.muted = false;
  audio.play();

  // Iniciar el nivel
  const nivel = laberintoDiv.dataset.nivel;
  iniciarNivel(nivel);

  // Iniciar la cuenta regresiva
  iniciarCuentaRegresiva();

  // Habilitar movimiento
  canMove = true;

  // Ocultar el botón "Start"
  startBtn.style.display = "none";
});

// Iniciar el nivel cuando se cargue la página, pero sin empezar la cuenta regresiva
document.addEventListener("DOMContentLoaded", () => {
  const nivel = laberintoDiv.dataset.nivel;
  iniciarNivel(nivel);
});